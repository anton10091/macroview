// [lon,lat] coords → Leaflet [lat,lon] LatLngs
const ll = (coords) => coords.map(([lo, la]) => [la, lo])
const geo = (coords) => ({ type:'Feature', properties:{}, geometry:{ type:'Polygon', coordinates:[coords] } })

export default function LeafletMap({ period }) {
  const divRef    = useRef(null)
  const mapRef    = useRef(null)
  const siamRef   = useRef(null)
  const lostRef   = useRef([])

  useEffect(() => {
    if (!divRef.current || mapRef.current) return

    const map = L.map(divRef.current, {
      center: [13.5, 101.5],
      zoom: 5,
      minZoom: 4,
      maxZoom: 8,
      scrollWheelZoom: false,
    })

    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
      attribution: '© OpenStreetMap',
      maxZoom: 8,
    }).addTo(map)

    // Modern Thailand — permanent dashed reference
    L.geoJSON(geo(COORDS_TH), {
      style: { color:'#2244bb', weight:2.5, dashArray:'8 5', fillOpacity:0, opacity:0.65 }
    }).bindTooltip('Современный Таиланд (для сравнения)', { sticky:true }).addTo(map)

    mapRef.current = map
    draw(map, period)

    return () => { map.remove(); mapRef.current = null }
  }, [])

  useEffect(() => {
    if (mapRef.current) draw(mapRef.current, period)
  }, [period])

  function draw(map, p) {
    if (siamRef.current) { siamRef.current.remove(); siamRef.current = null }
    lostRef.current.forEach(l => l.remove()); lostRef.current = []

    ;(HIST_LOST[p.year] || []).forEach(lt => {
      const layer = L.geoJSON(geo(lt.c), {
        style:{ color:'#cc3311', weight:1.5, dashArray:'6 4', fillColor:'#ee4422', fillOpacity:0.25, opacity:0.65 }
      }).bindTooltip(lt.n, { sticky:true }).addTo(map)
      lostRef.current.push(layer)
    })

    siamRef.current = L.geoJSON(geo(HIST_SIAM[p.year] || COORDS_TH), {
      style:{ color:'#a05818', weight:2.5, fillColor:'#e8a060', fillOpacity:0.6 }
    }).bindTooltip(`Сиам ${p.year} · ${p.km}`, { sticky:true }).addTo(map)
  }

  return <div ref={divRef} style={{ height:420, width:'100%', borderRadius:'12px 12px 0 0' }} />
}
